MOV AL, 10000000B
OUT 1FH, AL

START:

    MOV AL, 11111110B
    OUT 1BH, AL
    CALL DELAY


    MOV AL, 11111101B
    OUT 1BH, AL
    CALL DELAY

    MOV AL, 11111011B
    OUT 1BH, AL
    CALL DELAY

    MOV AL, 11110111B
    OUT 1BH, AL
    CALL DELAY

    JMP START

DELAY:
    MOV CX, 0FFFFH     
D1: LOOP D1
    RET
