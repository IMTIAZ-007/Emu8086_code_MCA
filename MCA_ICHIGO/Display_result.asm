.MODEL SMALL
.STACK 64H
.DATA
MSG1 DB 'fail','$'
MSG2 DB 'satisfactory','$'
MSG3 DB 'good','$'
MSG4 DB 'very good','$'
MSG5 DB 'excellent','$'
CLEAR MACRO
 MOV AX,0600H
 MOV BH,07
 MOV CX,0000
 MOV DX,184FH
 INT 10H
ENDM
CURSOR MACRO Col, Row
 MOV AH,02
 MOV BH,00
 MOV DL,Col
 MOV DH,Row
 INT 10H
ENDM
DISP MACRO MES
 MOV AH,09
 MOV DX,OFFSET MES
 INT 21H
ENDM
.CODE
MAIN:
 MOV AX,@DATA
 MOV DS,AX
AGAIN:
 CLEAR
 CURSOR 30,12
 MOV AH,01
 INT 21H
 CMP AL,'0'
 JB EXIT
 CMP AL,'9'
 JA EXIT
 SUB AL,30H
 MOV BL,AL
 MOV AH,01
 INT 21H
 CMP AL,'0'
 JB SHOW
 CMP AL,'9'
 JA SHOW
 SUB AL,30H
 MOV BH,AL
 MOV AL,BL
 MOV AH,0
 MOV BL,10
 MUL BL
 ADD AL,BH
SHOW:
 CMP AL,50
 JB F
 CMP AL,60
 JB S
 CMP AL,70
 JB G
 CMP AL,80
 JB V
 CMP AL,99
 JB E
 JMP AGAIN
F: CLEAR
   CURSOR 30,12
   DISP MSG1
   JMP AGAIN
S: CLEAR
   CURSOR 30,12
   DISP MSG2
   JMP AGAIN
G: CLEAR
   CURSOR 30,12
   DISP MSG3
   JMP AGAIN
V: CLEAR
   CURSOR 30,12
   DISP MSG4
   JMP AGAIN
E: CLEAR
   CURSOR 30,12
   DISP MSG5
   JMP AGAIN
EXIT:
 MOV AH,4CH
 INT 21H
END MAIN




