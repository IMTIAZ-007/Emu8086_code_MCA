START:
    MOV AL, 10000000B
    OUT 1EH, AL

    MOV AL, 11111111B
    OUT 18H, AL

MAIN_LOOP:
    MOV CX, 8
    MOV BL, 00000001B

NEXT_DIAG:
    MOV AL, BL
    OUT 1CH, AL

    MOV AL, BL
    NOT AL
    OUT 1AH, AL

    MOV AL, 11111111B
    OUT 1CH, AL
    OUT 1AH, AL

    SHL BL, 1
    LOOP NEXT_DIAG

 JMP MAIN_LOOP  ;EA 0C 10 00 00